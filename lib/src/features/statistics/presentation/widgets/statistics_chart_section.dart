                  Expanded(
                    child: Text(
                      _getLocalizedCardName(context, widget.cardStatistics.cardType),
                      style: theme.textTheme.titleSmall?.copyWith(
                        fontWeight: FontWeight.bold,
                        color: theme.colorScheme.onSurface,
                      ),

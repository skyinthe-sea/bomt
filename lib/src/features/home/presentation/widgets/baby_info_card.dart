        final elapsed = now.difference(startedAtLocal);
        setState(() {
          _elapsedMinutes = elapsed.inMinutes.abs(); // ÏùåÏàò Î∞©ÏßÄ
        });
        debugPrint('Sleep elapsed: ${elapsed.inMinutes} minutes (started: $startedAtLocal, now: $now)');
      }
    }
  }

